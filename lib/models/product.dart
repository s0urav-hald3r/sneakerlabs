class ProductModel {
  String name;
  String userName;
  int userFollowers;
  String description;
  int price;
  int size;
  bool isRotated;
  String userImage;
  String image;

  ProductModel({
    required this.name,
    required this.userName,
    required this.userFollowers,
    required this.description,
    required this.price,
    required this.size,
    required this.isRotated,
    required this.userImage,
    required this.image,
  });
}
